    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        try {
            String nome = txtNome.getText();
            funcionario.setNome(nome);
            String cpf = tctCpf.getText();
            funcionario.setCpf(cpf);
            String login = txtLogin.getText();
            funcionario.setLogin(login);
            String senha = String.valueOf(txtSenha.getPassword());
            funcionario.setSenha(senha);
            String email = txtEmail.getText();
            funcionario.setEmail(email);
            String cargo = txtCargo.getText();
            funcionario.setCargo(cargo);
            int nivel = Integer.parseInt(txtNivel.getText());
            funcionario.setNivel(nivel);
            conexao.conectar();
            conexao.cadastrarFuncionario(nome, cpf, login, senha, email, cargo, nivel);

            txtNome.setText("");
            tctCpf.setText("");
            txtLogin.setText("");
            txtSenha.setText("");
            txtEmail.setText("");
            txtCargo.setText("");
            txtNivel.setText("");
            txtNome.grabFocus();

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Campo nÃ£o pode ser vazio!");
        } finally {
            conexao.desconectar();
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

