    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        try {
            ArrayList<String> list = new ArrayList();

            Variable.cpf = tctCpf.getText();
            conexao.conectar();
            list = conexao.consultaFun();
            if (Variable.ckd) {
                lblId.setText(String.valueOf(list.get(0)));
                txtSenha.setText(list.get(1));
                txtLogin.setText(list.get(2));
                txtNome.setText(list.get(3));
                txtEmail.setText(list.get(4));
                txtCargo.setText(list.get(5));
                txtNivel.setText(list.get(6));
                Variable.cpf = "";
            } else {
                tctCpf.setText("");
                tctCpf.grabFocus();
                Variable.cpf = "";
            }
            Variable.ckd = false;
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Campos nÃ£o pode ser vazio");
        } finally {
            conexao.desconectar();
        }
    }//GEN-LAST:event_btnConsultarActionPerformed

